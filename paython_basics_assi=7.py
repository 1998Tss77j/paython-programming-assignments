"""3. Write a Python Program for array rotation?"""

def rotate_array(arr, k):
    """
    Rotate an array 'arr' by 'k' positions to the right.

    Parameters:
    arr (list): The input array to be rotated.
    k (int): The number of positions to rotate the array.

    Returns:
    list: The rotated array.
    """
    n = len(arr)
    k = k % n  # To handle cases where k > n

    # If k is 0 or equal to the array size, no rotation needed
    if k == 0 or k == n:
        return arr

    rotated = []
    for i in range(n - k, n):
        rotated.append(arr[i])

    for i in range(n - k):
        rotated.append(arr[i])

    return rotated


