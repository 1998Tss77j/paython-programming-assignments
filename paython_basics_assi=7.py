"""4. Write a Python Program to Split the array and add the first part to the end?"""

def split_and_add(arr, split_index):
    """
    Split the array 'arr' into two parts at the given 'split_index' and add the first part to the end.

    Parameters:
    arr (list): The input array to be split and added.
    split_index (int): The index at which the array is split.

    Returns:
    list: The modified array after splitting and adding.
    """
    n = len(arr)

    if split_index < 0 or split_index >= n:
        raise ValueError("Invalid split_index. It should be within the range of the array.")

    return arr[split_index:] + arr[:split_index]


# Example usage
if __name__ == "__main__":
    input_array = [1, 2, 3, 4, 5]
    split_index = 2
    modified_array = split_and_add(input_array, split_index)
    print("Original Array:", input_array)
    print("Modified Array:", modified_array)

