def is_monotonic(arr):
    """
    Check if the given array 'arr' is monotonic.

    Parameters:
    arr (list): The input array to check.

    Returns:
    bool: True if the array is monotonic, False otherwise.
    """
    increasing = decreasing = True

    for i in range(1, len(arr)):
        if arr[i] > arr[i - 1]:
            decreasing = False
        elif arr[i] < arr[i - 1]:
            increasing = False

    return increasing or decreasing


